ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "PAYMENT_REF" VARCHAR(100);
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "PAID_AT" TIMESTAMP(3);
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "PAID_BY" VARCHAR(100);
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "PAID_BY_ID" INTEGER;
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "EMERGENCY_RECEIVED_BY" VARCHAR(100);
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "EMERGENCY_RECEIVED_BY_ID" INTEGER;
ALTER TABLE "PRESCRIPTIONS" ADD COLUMN IF NOT EXISTS "EMERGENCY_DISPENSED_AT" TIMESTAMP(3);
