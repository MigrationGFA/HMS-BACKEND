CREATE TABLE IF NOT EXISTS "PATIENT_TRANSFER_EVENTS" (
    "EVENT_ID" SERIAL NOT NULL,
    "TRANSFER_ID" INTEGER NOT NULL,
    "EVENT_TYPE" VARCHAR(50) NOT NULL,
    "ACTOR_USER_ID" INTEGER,
    "ACTOR_LABEL" VARCHAR(150),
    "NOTE" TEXT,
    "OLD_STATUS" VARCHAR(40),
    "NEW_STATUS" VARCHAR(40),
    "CREATED_DATE" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "PATIENT_TRANSFER_EVENTS_pkey" PRIMARY KEY ("EVENT_ID")
);

CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFER_EVENTS_TRANSFER_ID_idx" ON "PATIENT_TRANSFER_EVENTS"("TRANSFER_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFER_EVENTS_CREATED_DATE_idx" ON "PATIENT_TRANSFER_EVENTS"("CREATED_DATE");

DO $$ BEGIN
  ALTER TABLE "PATIENT_TRANSFER_EVENTS" ADD CONSTRAINT "PATIENT_TRANSFER_EVENTS_TRANSFER_ID_fkey"
    FOREIGN KEY ("TRANSFER_ID") REFERENCES "PATIENT_TRANSFERS"("TRANSFER_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

CREATE TABLE IF NOT EXISTS "NOTIFICATIONS" (
    "NOTIFICATION_ID" SERIAL NOT NULL,
    "USER_ID" INTEGER NOT NULL,
    "ROLE_HINT" VARCHAR(50),
    "TYPE" VARCHAR(60) NOT NULL,
    "TITLE" VARCHAR(255) NOT NULL,
    "BODY" TEXT,
    "LINK_PATH" VARCHAR(255),
    "ENTITY" VARCHAR(80),
    "ENTITY_ID" INTEGER,
    "PERSON_ID" INTEGER,
    "IS_READ" BOOLEAN NOT NULL DEFAULT false,
    "ACKNOWLEDGED_AT" TIMESTAMP(3),
    "CREATED_DATE" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "NOTIFICATIONS_pkey" PRIMARY KEY ("NOTIFICATION_ID")
);

CREATE INDEX IF NOT EXISTS "NOTIFICATIONS_USER_ID_IS_READ_CREATED_DATE_idx"
  ON "NOTIFICATIONS"("USER_ID", "IS_READ", "CREATED_DATE");
CREATE INDEX IF NOT EXISTS "NOTIFICATIONS_TYPE_idx" ON "NOTIFICATIONS"("TYPE");
CREATE INDEX IF NOT EXISTS "NOTIFICATIONS_PERSON_ID_idx" ON "NOTIFICATIONS"("PERSON_ID");

DO $$ BEGIN
  ALTER TABLE "NOTIFICATIONS" ADD CONSTRAINT "NOTIFICATIONS_USER_ID_fkey"
    FOREIGN KEY ("USER_ID") REFERENCES "USERS"("USER_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL; END $$;
