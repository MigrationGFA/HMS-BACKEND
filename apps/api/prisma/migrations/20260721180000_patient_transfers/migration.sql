-- Patient Transfer Management + in-app Notifications

CREATE TABLE IF NOT EXISTS "PATIENT_TRANSFERS" (
    "TRANSFER_ID" SERIAL NOT NULL,
    "TRANSFER_NO" VARCHAR(50) NOT NULL,
    "PERSON_ID" INTEGER NOT NULL,
    "ADMISSION_ID" INTEGER,
    "TRANSFER_TYPE" VARCHAR(40) NOT NULL,
    "PRIORITY" VARCHAR(30) NOT NULL DEFAULT 'Routine',
    "FROM_WARD_ID" INTEGER,
    "TO_WARD_ID" INTEGER,
    "TO_WARD_PREFERENCE" VARCHAR(150),
    "DESTINATION_LABEL" VARCHAR(150),
    "ALLOCATED_BED_ID" INTEGER,
    "REASON" TEXT NOT NULL,
    "CLINICAL_NOTES" TEXT,
    "HANDOVER_NOTES" TEXT,
    "STATUS" VARCHAR(40) NOT NULL DEFAULT 'Submitted',
    "REQUESTED_BY_USER_ID" INTEGER,
    "PREPARED_BY_USER_ID" INTEGER,
    "PREPARED_AT" TIMESTAMP(3),
    "ALLOCATED_BY_USER_ID" INTEGER,
    "ALLOCATED_AT" TIMESTAMP(3),
    "ACCEPTED_BY_USER_ID" INTEGER,
    "ACCEPTED_AT" TIMESTAMP(3),
    "DEPARTED_BY_USER_ID" INTEGER,
    "DEPARTED_AT" TIMESTAMP(3),
    "RECEIVED_BY_USER_ID" INTEGER,
    "RECEIVED_AT" TIMESTAMP(3),
    "REJECTION_REASON" TEXT,
    "EXTERNAL_FACILITY" VARCHAR(255),
    "CREATED_BY_ID" INTEGER,
    "CREATED_BY" VARCHAR(100),
    "CREATED_DATE" TIMESTAMP(3),
    "UPDATED_BY_ID" INTEGER,
    "UPDATED_BY" VARCHAR(100),
    "UPDATED_DATE" TIMESTAMP(3),
    CONSTRAINT "PATIENT_TRANSFERS_pkey" PRIMARY KEY ("TRANSFER_ID")
);

CREATE UNIQUE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_TRANSFER_NO_key" ON "PATIENT_TRANSFERS"("TRANSFER_NO");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_PERSON_ID_idx" ON "PATIENT_TRANSFERS"("PERSON_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_ADMISSION_ID_idx" ON "PATIENT_TRANSFERS"("ADMISSION_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_STATUS_idx" ON "PATIENT_TRANSFERS"("STATUS");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_FROM_WARD_ID_idx" ON "PATIENT_TRANSFERS"("FROM_WARD_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_TO_WARD_ID_idx" ON "PATIENT_TRANSFERS"("TO_WARD_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_REQUESTED_BY_USER_ID_idx" ON "PATIENT_TRANSFERS"("REQUESTED_BY_USER_ID");
CREATE INDEX IF NOT EXISTS "PATIENT_TRANSFERS_CREATED_DATE_idx" ON "PATIENT_TRANSFERS"("CREATED_DATE");

