    FOREIGN KEY ("AUTHOR_ID") REFERENCES "USERS"("USER_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

DO $$ BEGIN
  ALTER TABLE "CLINICAL_NOTE_VERSIONS" ADD CONSTRAINT "CLINICAL_NOTE_VERSIONS_CLINICAL_NOTE_ID_fkey"
    FOREIGN KEY ("CLINICAL_NOTE_ID") REFERENCES "CLINICAL_NOTES"("CLINICAL_NOTE_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;
