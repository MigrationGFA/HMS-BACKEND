EXCEPTION WHEN duplicate_object THEN NULL; END $$;

DO $$ BEGIN
  ALTER TABLE "CLINICAL_REFERRALS" ADD CONSTRAINT "CLINICAL_REFERRALS_REQUESTED_BY_USER_ID_fkey"
    FOREIGN KEY ("REQUESTED_BY_USER_ID") REFERENCES "USERS"("USER_ID") ON DELETE SET NULL ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

DO $$ BEGIN
  ALTER TABLE "CLINICAL_REFERRALS" ADD CONSTRAINT "CLINICAL_REFERRALS_TO_DOCTOR_USER_ID_fkey"
    FOREIGN KEY ("TO_DOCTOR_USER_ID") REFERENCES "USERS"("USER_ID") ON DELETE SET NULL ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

CREATE TABLE IF NOT EXISTS "CLINICAL_REFERRAL_EVENTS" (
    "EVENT_ID" SERIAL NOT NULL,
    "REFERRAL_ID" INTEGER NOT NULL,
    "EVENT_TYPE" VARCHAR(50) NOT NULL,
    "ACTOR_USER_ID" INTEGER,
    "ACTOR_LABEL" VARCHAR(150),
    "NOTE" TEXT,
    "OLD_STATUS" VARCHAR(40),
    "NEW_STATUS" VARCHAR(40),
    "CREATED_DATE" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "CLINICAL_REFERRAL_EVENTS_pkey" PRIMARY KEY ("EVENT_ID")
);

CREATE INDEX IF NOT EXISTS "CLINICAL_REFERRAL_EVENTS_REFERRAL_ID_idx" ON "CLINICAL_REFERRAL_EVENTS"("REFERRAL_ID");
CREATE INDEX IF NOT EXISTS "CLINICAL_REFERRAL_EVENTS_CREATED_DATE_idx" ON "CLINICAL_REFERRAL_EVENTS"("CREATED_DATE");

DO $$ BEGIN
  ALTER TABLE "CLINICAL_REFERRAL_EVENTS" ADD CONSTRAINT "CLINICAL_REFERRAL_EVENTS_REFERRAL_ID_fkey"
    FOREIGN KEY ("REFERRAL_ID") REFERENCES "CLINICAL_REFERRALS"("REFERRAL_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL; END $$;
