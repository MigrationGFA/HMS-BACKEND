    FOREIGN KEY ("PRESCRIPTION_ID") REFERENCES "PRESCRIPTIONS"("PRESCRIPTION_ID") ON DELETE RESTRICT ON UPDATE CASCADE;
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

