    "FULL_NAME" VARCHAR(200) NOT NULL,
    "PHONE" VARCHAR(40) NOT NULL,
    "ADDRESS" VARCHAR(255),
    "BLOOD_GROUP" VARCHAR(10),
    "NOTES" TEXT,
    "STATUS" VARCHAR(30) NOT NULL DEFAULT 'Active',
    "CREATED_BY_ID" INTEGER,
    "CREATED_BY" VARCHAR(100),
    "CREATED_DATE" TIMESTAMP(3),
    "UPDATED_BY_ID" INTEGER,
    "UPDATED_BY" VARCHAR(100),
    "UPDATED_DATE" TIMESTAMP(3),
    "DELETED_FLAG" VARCHAR(1) DEFAULT 'N'
);

CREATE UNIQUE INDEX IF NOT EXISTS "BLOOD_DONORS_DONOR_NO_key" ON "BLOOD_DONORS"("DONOR_NO");
CREATE INDEX IF NOT EXISTS "BLOOD_DONORS_PHONE_idx" ON "BLOOD_DONORS"("PHONE");
CREATE INDEX IF NOT EXISTS "BLOOD_DONORS_STATUS_idx" ON "BLOOD_DONORS"("STATUS");
CREATE INDEX IF NOT EXISTS "BLOOD_DONORS_FULL_NAME_idx" ON "BLOOD_DONORS"("FULL_NAME");

