CREATE INDEX IF NOT EXISTS "NURSING_OBSERVATIONS_PERSON_ID_idx" ON "NURSING_OBSERVATIONS"("PERSON_ID");
CREATE INDEX IF NOT EXISTS "NURSING_OBSERVATIONS_ADMISSION_ID_idx" ON "NURSING_OBSERVATIONS"("ADMISSION_ID");
CREATE INDEX IF NOT EXISTS "NURSING_OBSERVATIONS_RECORDED_AT_idx" ON "NURSING_OBSERVATIONS"("RECORDED_AT");

CREATE TABLE IF NOT EXISTS "NURSING_INCIDENTS" (
  "INCIDENT_ID" SERIAL PRIMARY KEY,
  "ADMISSION_ID" INTEGER,
