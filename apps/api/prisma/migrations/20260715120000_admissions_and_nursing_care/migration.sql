CREATE TABLE IF NOT EXISTS "NURSING_FORM_TEMPLATES" (
  "TEMPLATE_ID" SERIAL PRIMARY KEY,
  "CODE" VARCHAR(50) NOT NULL UNIQUE,
  "TITLE" VARCHAR(255) NOT NULL,
  "SCHEMA_JSON" TEXT NOT NULL,
  "ACTIVE" BOOLEAN NOT NULL DEFAULT true,
  "CREATED_BY" VARCHAR(100),
  "CREATED_DATE" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  "UPDATED_DATE" TIMESTAMP(3)
);

CREATE TABLE IF NOT EXISTS "NURSING_FORM_INSTANCES" (
  "INSTANCE_ID" SERIAL PRIMARY KEY,
  "TEMPLATE_ID" INTEGER NOT NULL,
  "ADMISSION_ID" INTEGER,
  "PERSON_ID" INTEGER NOT NULL,
  "VALUES_JSON" TEXT NOT NULL,
  "SIGNED_BY" VARCHAR(100),
  "CREATED_DATE" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT "NURSING_FORM_INSTANCES_TEMPLATE_ID_fkey" FOREIGN KEY ("TEMPLATE_ID") REFERENCES "NURSING_FORM_TEMPLATES"("TEMPLATE_ID") ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT "NURSING_FORM_INSTANCES_ADMISSION_ID_fkey" FOREIGN KEY ("ADMISSION_ID") REFERENCES "ADMISSIONS"("ADMISSION_ID") ON DELETE SET NULL ON UPDATE CASCADE,
  CONSTRAINT "NURSING_FORM_INSTANCES_PERSON_ID_fkey" FOREIGN KEY ("PERSON_ID") REFERENCES "PERSONS"("PERSON_ID") ON DELETE RESTRICT ON UPDATE CASCADE
);
CREATE INDEX IF NOT EXISTS "NURSING_FORM_INSTANCES_PERSON_ID_idx" ON "NURSING_FORM_INSTANCES"("PERSON_ID");
CREATE INDEX IF NOT EXISTS "NURSING_FORM_INSTANCES_TEMPLATE_ID_idx" ON "NURSING_FORM_INSTANCES"("TEMPLATE_ID");
