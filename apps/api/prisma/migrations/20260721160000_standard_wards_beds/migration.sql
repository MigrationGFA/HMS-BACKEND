-- Backfill WARD_ID from legacy WARDS_ID
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'BEDS' AND column_name = 'WARDS_ID'
  ) THEN
    EXECUTE 'UPDATE "BEDS" SET "WARD_ID" = COALESCE("WARD_ID", "WARDS_ID") WHERE "WARD_ID" IS NULL';
  END IF;
END $$;

-- Backfill LABEL from legacy BED_NO
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'BEDS' AND column_name = 'BED_NO'
  ) THEN
    EXECUTE 'UPDATE "BEDS" SET "LABEL" = COALESCE(NULLIF(TRIM("LABEL"), ''''), NULLIF(TRIM("BED_NO"), ''''), ''BED-'' || "BED_ID"::text) WHERE "LABEL" IS NULL OR TRIM("LABEL") = ''''';
  ELSE
    UPDATE "BEDS"
    SET "LABEL" = COALESCE(NULLIF(TRIM("LABEL"), ''), 'BED-' || "BED_ID"::text)
    WHERE "LABEL" IS NULL OR TRIM("LABEL") = '';
  END IF;
END $$;

-- Backfill STATUS from legacy AVAILABILITY (or default AVAILABLE)
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'BEDS' AND column_name = 'AVAILABILITY'
  ) THEN
    EXECUTE $u$
      UPDATE "BEDS" SET "STATUS" = CASE
        WHEN "STATUS" IS NOT NULL AND TRIM("STATUS") <> '' THEN UPPER(TRIM("STATUS"))
        WHEN UPPER(TRIM(COALESCE("AVAILABILITY", ''))) IN ('AVAILABLE', 'YES', 'FREE', '1', 'TRUE') THEN 'AVAILABLE'
        WHEN UPPER(TRIM(COALESCE("AVAILABILITY", ''))) IN ('OCCUPIED', 'NO', 'TAKEN', '0', 'FALSE') THEN 'OCCUPIED'
        WHEN UPPER(TRIM(COALESCE("AVAILABILITY", ''))) IN ('RESERVED') THEN 'RESERVED'
        WHEN UPPER(TRIM(COALESCE("AVAILABILITY", ''))) IN ('MAINTENANCE', 'OUT_OF_SERVICE', 'OOS') THEN 'OUT_OF_SERVICE'
        ELSE 'AVAILABLE'
      END
      WHERE "STATUS" IS NULL OR TRIM("STATUS") = ''
    $u$;
  ELSE
    UPDATE "BEDS"
    SET "STATUS" = COALESCE(NULLIF(TRIM("STATUS"), ''), 'AVAILABLE')
    WHERE "STATUS" IS NULL OR TRIM("STATUS") = '';
  END IF;
END $$;

UPDATE "BEDS" SET "CREATED_DATE" = COALESCE("CREATED_DATE", NOW());
UPDATE "BEDS" SET "CREATED_BY" = COALESCE("CREATED_BY", 'SYSTEM');

