ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "ADMISSION_DEPOSIT_DEFAULT" DECIMAL(14,2) NOT NULL DEFAULT 0;
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "STATUS" VARCHAR(30);
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "CREATED_BY" VARCHAR(100);
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "CREATED_DATE" TIMESTAMP(3);
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "UPDATED_BY" VARCHAR(100);
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "UPDATED_DATE" TIMESTAMP(3);

-- Backfill NAME from legacy WARD_NAME when present
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'WARDS' AND column_name = 'WARD_NAME'
  ) THEN
    EXECUTE 'UPDATE "WARDS" SET "NAME" = COALESCE(NULLIF(TRIM("NAME"), ''''), NULLIF(TRIM("WARD_NAME"), ''''), ''Ward '' || "WARD_ID"::text) WHERE "NAME" IS NULL OR TRIM("NAME") = ''''';
  ELSE
    UPDATE "WARDS"
    SET "NAME" = COALESCE(NULLIF(TRIM("NAME"), ''), 'Ward ' || "WARD_ID"::text)
    WHERE "NAME" IS NULL OR TRIM("NAME") = '';
  END IF;
END $$;

-- Backfill CODE if empty
UPDATE "WARDS"
SET "CODE" = COALESCE(NULLIF(TRIM("CODE"), ''), 'W' || LPAD("WARD_ID"::text, 4, '0'))
WHERE "CODE" IS NULL OR TRIM("CODE") = '';

UPDATE "WARDS" SET "STATUS" = COALESCE(NULLIF(TRIM("STATUS"), ''), 'Active');
UPDATE "WARDS" SET "GENDER" = COALESCE(NULLIF(TRIM("GENDER"), ''), 'Mixed');
UPDATE "WARDS" SET "CREATED_DATE" = COALESCE("CREATED_DATE", NOW());
UPDATE "WARDS" SET "CREATED_BY" = COALESCE("CREATED_BY", 'SYSTEM');

ALTER TABLE "WARDS" ALTER COLUMN "CODE" SET NOT NULL;
ALTER TABLE "WARDS" ALTER COLUMN "NAME" SET NOT NULL;
ALTER TABLE "WARDS" ALTER COLUMN "STATUS" SET NOT NULL;
ALTER TABLE "WARDS" ALTER COLUMN "STATUS" SET DEFAULT 'Active';
ALTER TABLE "WARDS" ALTER COLUMN "GENDER" SET NOT NULL;
ALTER TABLE "WARDS" ALTER COLUMN "GENDER" SET DEFAULT 'Mixed';

