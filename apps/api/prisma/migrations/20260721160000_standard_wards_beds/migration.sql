-- Root cause of empty Records admit dropdowns: CREATE TABLE IF NOT EXISTS skipped when legacy
-- WARDS/BEDS already existed (WARD_NAME / WARDS_ID / BED_NO / AVAILABILITY), so CODE/NAME/WARD_ID/LABEL/STATUS were missing.

-- ─── WARDS: ensure inpatient columns ─────────────────────────────────────────
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "CODE" VARCHAR(50);
ALTER TABLE "WARDS" ADD COLUMN IF NOT EXISTS "NAME" VARCHAR(150);
